    public void listaAnimais(Animal[] animais){
        DefaultTableModel dtm = (DefaultTableModel) jTAnimais.getModel();
        
        ((DefaultTableModel) jTAnimais.getModel()).setNumRows(0);
        jTAnimais.updateUI();
        
        for(Animal a: animais){
                if(a!=null)
                    dtm.addRow(new Object[]{a.getNome()});
        }
    }

