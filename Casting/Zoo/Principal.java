    private void jBSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSalvarActionPerformed
         // TODO add your handling code here
         boolean resultado = false;
         String nome = jTFNome.getText();
         float peso = Float.valueOf(jTFPeso.getText());
         if(jCBTipoAnimal.getSelectedIndex()==0){
             Boolean voa = (jCBVoar.getSelectedItem().equals("SIM"));
             int envergadura = Integer.parseInt(jTFEnvergadura.getText());
             Passaro p = new Passaro(nome, peso, voa, envergadura);
             resultado = zoo.adicionarAnimal(p);
         }else{
                int idade = Integer.parseInt(jTFIdade.getText());
                int velocidade = Integer.parseInt(jTFVelocidade.getText());
                if(jCBTipoAnimal.getSelectedIndex()==1){
                    float altura = Float.parseFloat(jTFAltura.getText());
                    Cachorro ch = new Cachorro(nome, peso, idade, velocidade, altura);
                    resultado = zoo.adicionarAnimal(ch);
                }
                if(jCBTipoAnimal.getSelectedIndex()==2){
                    String raca = jTFAltura.getText();
                    Cavalo cv = new Cavalo(nome, peso, idade, velocidade, raca);
                    resultado = zoo.adicionarAnimal(cv);
                }
             
         }
         if (resultado)
             JOptionPane.showMessageDialog(null, "Animal salvo com sucesso");
         else
             JOptionPane.showMessageDialog(null, "Falha no salvamento");
         limpar();
         buscar();
    }//GEN-LAST:event_jBSalvarActionPerformed

    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        // TODO add your handling code here:
        String nome = jTFNome.getText();
        float peso = Float.valueOf(jTFPeso.getText());
        int tipo;
        if(jCBTipoAnimal.getSelectedItem().equals("Pássaro")){
            tipo = 1;
        }else{
            if(jCBTipoAnimal.getSelectedItem().equals("Cachorro")){
                tipo = 2;
            }else{
                tipo = 3;
            }
        }
        Animal animal =  new Animal(nome, peso, tipo);
        if(zoo.excluirAnimal(animal))
            JOptionPane.showMessageDialog(null, "Excluído com sucesso");
        else
            JOptionPane.showMessageDialog(null, "Animal não encontrado");
        limpar();
        buscar();
    }//GEN-LAST:event_jBExcluirActionPerformed

