    private void jTAnimaisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTAnimaisMouseClicked
        // TODO add your handling code here:
        int linha = jTAnimais.getSelectedRow();
        String nome = (String) jTAnimais.getValueAt(linha, 0);
        Animal a = zoo.buscaAnimal(nome);
        if (a==null) return;
        if(a.getTipo()==1){
            Passaro p = (Passaro) a;
            limpar();
            
            jCBTipoAnimal.setSelectedIndex(0);
            
            jCBVoar.setEnabled(true);
            jTFAltura.setEnabled(false);
            jTFEnvergadura.setEnabled(true);
            jTFIdade.setEnabled(false);
            jTFNome.setEnabled(true);
            jTFPeso.setEnabled(true);
            jTFVelocidade.setEnabled(false);
            
            if(p.getVoa()) 
                jCBVoar.setSelectedIndex(0);
            else
                jCBVoar.setSelectedIndex(1);
            jTFEnvergadura.setText(String.valueOf(p.getEnvergadura()));
            jTFNome.setText(p.getNome());
            jTFPeso.setText(String.valueOf(p.getPeso()));
        }
        
        if(a.getTipo()==2){
            Cachorro ch = (Cachorro) a;
            limpar();
            
            jCBTipoAnimal.setSelectedIndex(1);
            
            jCBVoar.setEnabled(false);
            jTFAltura.setEnabled(true);
            jTFEnvergadura.setEnabled(false);
            jTFIdade.setEnabled(true);
            jTFNome.setEnabled(true);
            jTFPeso.setEnabled(true);
            jTFVelocidade.setEnabled(true);
            jLAlturaRaca.setText("Altura");
            
            jTFAltura.setText(String.valueOf(ch.getAltura()));
            jTFIdade.setText(String.valueOf(ch.getIdade()));
            jTFNome.setText(ch.getNome());
            jTFPeso.setText(String.valueOf(ch.getPeso()));
            jTFVelocidade.setText(String.valueOf(ch.getVelocidade()));
        }
        
        if(a.getTipo()==3){ 
            Cavalo cv = (Cavalo) a;
            limpar();
            
            jCBTipoAnimal.setSelectedIndex(2);
            
            jCBVoar.setEnabled(false);
            jTFAltura.setEnabled(true);
            jTFEnvergadura.setEnabled(false);
            jTFIdade.setEnabled(true);
            jTFNome.setEnabled(true);
            jTFPeso.setEnabled(true);
            jTFVelocidade.setEnabled(true);
            jLAlturaRaca.setText("Raça   ");
            
            jTFAltura.setText(cv.getRaca());
            jTFIdade.setText(String.valueOf(cv.getIdade()));
            jTFNome.setText(cv.getNome());
            jTFPeso.setText(String.valueOf(cv.getPeso()));
            jTFVelocidade.setText(String.valueOf(cv.getVelocidade()));
        }
    }//GEN-LAST:event_jTAnimaisMouseClicked

